# Day 1: Variables and Data Types

# 1. Basic variable assignment
animal = "dog"
name = "Charlie"
age = 3
hobby = "playing fetch"
is_adult = age >= 3

print("My pet is a", animal)
print("Its name is", name)
print(name, "is", age, "years old.")
print("It loves", hobby)
print("Is it an adult?", is_adult)

# 2. Updating variables
hobby = "eating snacks"
print("Actually, it now loves", hobby)

# 3. Variable types
print(type(animal))   # str
print(type(age))      # int
print(type(is_adult)) # bool
